local opt = vim.opt

-- hybrid line numbers
opt.relativenumber = true
opt.number = true

-- indenting
opt.expandtab = true
opt.tabstop = 2
opt.softtabstop = 2
opt.shiftwidth = 2

opt.clipboard = 'unnamed,unnamedplus'

-- split windows
opt.splitright = true
opt.splitbelow = true

-- no swap files
opt.swapfile = false
